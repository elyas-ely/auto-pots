// Function to send a post to the API
const sendUser = async (userData) => {
  try {
    const response = await fetch(api, {
      method: 'POST',
      headers: { 'Content-Type': 'application/json' },
      body: JSON.stringify(userData),
    })

    if (!response.ok) throw new Error(`Error: ${response.statusText}`)

    return response.json()
  } catch (error) {
    console.error('Failed to send post:', error)
    return null
  }
}

// Function to send multiple posts concurrently using Promise.all()
const sendBulkUsers = async (count) => {
  try {
    const users = Array.from({ length: count }, generateRandomPost) // Generate users

    const postPromises = users.map(sendUser)
    const results = await Promise.all(postPromises)

    console.log(
      `Successfully sent ${results.filter(Boolean).length}/${count} users.`
    )
  } catch (error) {
    console.error('Error sending bulk users:', error)
  }
}

